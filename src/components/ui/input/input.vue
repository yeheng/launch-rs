<template>
  <input
    :value="value"
    @input="$emit('input', $event)"
    :type="type"
    :placeholder="placeholder"
    :disabled="disabled"
    :class="[
      'flex h-9 w-full rounded-md border border-gray-300 bg-transparent px-3 py-1 text-sm shadow-sm transition-colors placeholder:text-gray-500 focus-visible:outline-none focus-visible:ring-1 focus-visible:ring-blue-500 disabled:cursor-not-allowed disabled:opacity-50',
      className
    ]"
  />
</template>

<script setup lang="ts">
interface Props {
  value?: string | number
  type?: string
  placeholder?: string
  disabled?: boolean
  className?: string
}

interface Emits {
  (e: 'input', event: Event): void
}

withDefaults(defineProps<Props>(), {
  type: 'text',
  disabled: false
})

defineEmits<Emits>()
</script>